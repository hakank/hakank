@model function changepoint(ys)
    N = length(ys)
    mu = mean(ys)

    mu1 ~ Uniform(0, mu)
    mu2 ~ Uniform(mu, 10*mu)
    cp ~ DiscreteUniform(1,N)

    for i in 1:N
        ys[i] ~ i < cp ? Normal(mu1,1) : Normal(mu2,1)
    end

end

