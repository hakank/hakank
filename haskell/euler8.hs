euler8a :: Int
euler8a = maximum [ product [p8 !! (i+j) | i <- [0..4]] | j <- [1..(1000-5)]]

-- much faster than euler8a
euler8b :: Int
euler8b = maximum $ map (\i -> product $ take 5 $ drop i p8) [0..1000-4]

-- much faster than euler8b
euler8c' :: [Int] -> [Int]
euler8c' [] = [] -- This generates some [0]'s but it's faster than using guards to check lengths etc
-- It's slower to use alias, i.e. a@(x:xs) = .(.. take 5 a)
euler8c' (x:xs) = (product $ take 5 (x:xs)) : euler8c' xs

euler8c :: Int
euler8c = maximum $ euler8c' p8

-- Same idea as euler8c but using the general version of running product (defined in HakankUtils.curry)
euler8d = maximum $ runningProd 5 p8

