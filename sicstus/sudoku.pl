solve(P) :-
        problem(P,Problem,CellSize),
        format('\nProblem ~w Cell size: ~d\n',[P,CellSize]),
        findall(Problem,(sudoku(Problem,CellSize),write_matrix(Problem)),_),
        fd_statistics.


write_matrix(Matrix) :-
        ( foreach(Row, Matrix) do
              write(Row), nl
        ).


sudoku(Problem,CellSize) :-
        
        append(Problem, Vars),
        N is CellSize*CellSize,
        domain(Vars, 1,N),

        % rows
        ( foreach(Row, Problem) do
              all_distinct(Row)
        ),
        
        % columns
        transpose(Problem,Columns),
        ( foreach(Column, Columns) do
              all_distinct(Column)
        ),

        % cells
        CellSize1 is CellSize-1,
        ( for(I, 0, CellSize1), 
          param(Vars,CellSize,CellSize1,N) do
              ( for(J, 0, CellSize1), 
                param(I,Vars,CellSize,CellSize1,N) do
                    ( for(R, I*CellSize,I*CellSize+CellSize1), 
                      fromto(RR, OutR, InR, []),
                      param(I,J,Vars,CellSize,CellSize1,N) do
                          ( for(C, J*CellSize,J*CellSize+CellSize1), 
                            fromto(CC, OutC, InC, []),
                            param(I,J,R,Vars,N) do
                                X is 1+R*N+C,
                                element(X, Vars, El),
                                OutC = [El|InC]
                          ),
                          OutR = [CC|InR]
                    ),
                    append(RR,V),
                    all_distinct(V)
              )
        ),

        labeling([ffc,enum,down], Vars).


